document.addEventListener("click", (event) => {
  const openButton = event.target.closest("[data-open-lesson]");
  if (openButton) {
    openLesson(openButton.dataset.openLesson);
    return;
  }

  const tabButton = event.target.closest("[data-lesson-tab]");
  if (tabButton) {
    setLessonTab(tabButton.dataset.lessonTab);
    return;
  }

  const lessonAnswerButton = event.target.closest("[data-lesson-answer]");
  if (lessonAnswerButton) {
    checkLessonAnswer(Number(lessonAnswerButton.dataset.lessonAnswer));
    return;
  }

  const practiceMoveButton = event.target.closest("[data-practice-move]");
  if (practiceMoveButton && !practiceMoveButton.disabled) {
    moveLessonPractice(Number(practiceMoveButton.dataset.practiceMove));
    return;
  }

  const viewButton = event.target.closest("[data-view-link]");
  if (viewButton) {
    setView(viewButton.dataset.viewLink);
    return;
  }

  const completeButton = event.target.closest("[data-complete]");
  if (completeButton) {
    toggleLesson(completeButton.dataset.complete);
  }
});

