function renderPracticeTab(lesson) {
  const progress = getPracticeState(lesson.id);
  const questionIndex = Math.min(progress.index, lesson.practice.length - 1);
  progress.index = questionIndex;
  const current = lesson.practice[questionIndex];
  const selectedRaw = progress.answered[String(questionIndex)];
  const selected = selectedRaw === undefined ? null : Number(selectedRaw);
  const answered = selected !== null;
  const score = getLessonScore(lesson.id);

  return `
    <div class="lesson-practice-panel">
      <div class="practice-topline">
        <span>Check ${questionIndex + 1} / ${lesson.practice.length}</span>
        <strong>${score.correct} correct</strong>
      </div>
      <h3>${escapeHtml(current.question)}</h3>
      <div class="answer-grid lesson-answer-grid">
        ${current.answers.map((answer, index) => {
          const isCorrect = index === current.correct;
          const isSelected = answered && selected === index;
          const stateClass = answered
            ? isCorrect
              ? "correct"
              : isSelected
                ? "wrong"
                : ""
            : "";
          return `
            <button class="answer-option ${stateClass}" type="button" data-lesson-answer="${index}" ${answered ? "disabled" : ""}>
              <span class="answer-letter">${String.fromCharCode(65 + index)}</span>
              ${escapeHtml(answer)}
            </button>
          `;
        }).join("")}
      </div>
      <p class="feedback lesson-feedback" aria-live="polite">
        ${answered
          ? selected === current.correct
            ? `Correct. ${escapeHtml(current.note)}`
            : `Not quite. ${escapeHtml(current.note)}`
          : "Choose an answer to check this lesson point."}
      </p>
      <div class="lesson-practice-actions">
        <button class="ghost-action" type="button" data-practice-move="-1" ${questionIndex === 0 ? "disabled" : ""}>
          <i data-lucide="arrow-left"></i>
          Previous
        </button>
        <button class="primary-action" type="button" data-practice-move="1" ${questionIndex === lesson.practice.length - 1 ? "disabled" : ""}>
          <i data-lucide="arrow-right"></i>
          Next check
        </button>
      </div>
    </div>
  `;
}

