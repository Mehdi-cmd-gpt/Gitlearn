function renderLessonStudio() {
  const lesson = getLesson();
  const score = getLessonScore(lesson.id);
  const done = state.completedLessons.includes(lesson.id);
  const percent = score.total ? Math.round((score.attempted / score.total) * 100) : 0;

  studioUnit.textContent = lesson.type;
  studioScore.textContent = `${score.correct} / ${score.total}`;
  studioPulse.textContent =
    score.attempted === score.total
      ? "All quick checks attempted. Review the writing plan, then mark the unit done."
      : "Open a tab, study the explanation, then answer the quick checks.";
  studioProgress.style.width = `${percent}%`;
  studioTitle.textContent = lesson.title;
  studioSummary.textContent = lesson.text;
  studioComplete.dataset.complete = lesson.id;
  studioComplete.classList.toggle("done", done);
  studioComplete.innerHTML = `
    <i data-lucide="${done ? "check" : "check-circle"}"></i>
    ${done ? "Completed" : "Mark done"}
  `;

  studioMeta.innerHTML = [
    ["Function", lesson.functions],
    ["Grammar", lesson.grammar],
    ["Writing", lesson.writing],
    ["Exam drill", lesson.examTask],
  ]
    .map(([label, value]) => `
      <div>
        <span>${escapeHtml(label)}</span>
        <p>${escapeHtml(value)}</p>
      </div>
    `)
    .join("");

  lessonTabButtons.forEach((button) => {
    const active = button.dataset.lessonTab === state.activeLessonTab;
    button.classList.toggle("active", active);
    button.setAttribute("aria-selected", String(active));
  });

  renderStudioBody(lesson);
}

function renderStudioBody(lesson) {
  if (state.activeLessonTab === "vocabulary") {
    studioBody.innerHTML = renderVocabularyTab(lesson);
  } else if (state.activeLessonTab === "practice") {
    studioBody.innerHTML = renderPracticeTab(lesson);
  } else if (state.activeLessonTab === "writing") {
    studioBody.innerHTML = renderWritingTab(lesson);
  } else {
    studioBody.innerHTML = renderExplainTab(lesson);
  }
}

