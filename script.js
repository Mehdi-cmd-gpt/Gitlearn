function getPracticeState(lessonId) {
  if (!state.lessonPractice[lessonId]) {
    state.lessonPractice[lessonId] = { index: 0, answered: {} };
  }
  const progress = state.lessonPractice[lessonId];
  progress.index = Number(progress.index || 0);
  progress.answered = progress.answered || {};
  return progress;
}

function getLessonScore(lessonId) {
  const lesson = getLesson(lessonId);
  const progress = getPracticeState(lesson.id);
  const answeredKeys = Object.keys(progress.answered);
  const correct = answeredKeys.filter((key) => {
    const question = lesson.practice[Number(key)];
    return question && Number(progress.answered[key]) === question.correct;
  }).length;

  return {
    correct,
    attempted: answeredKeys.length,
    total: lesson.practice.length,
  };
}

function getAllLessonPracticeProgress() {
  return lessons.reduce(
    (total, lesson) => {
      const score = getLessonScore(lesson.id);
      total.attempted += score.attempted;
      total.questions += score.total;
      return total;
    },
    { attempted: 0, questions: 0 },
  );
}

function updateProgress() {
  const lessonProgress = state.completedLessons.length / lessons.length;
  const practiceProgress = getAllLessonPracticeProgress();
  const studioProgressValue = practiceProgress.questions
    ? practiceProgress.attempted / practiceProgress.questions
    : 0;
  const quizProgress = Math.min(state.answered / questions.length, 1);
  const total = Math.round(((lessonProgress * 0.5) + (studioProgressValue * 0.25) + (quizProgress * 0.25)) * 100);

  progressScore.textContent = `${total}%`;
  scoreFill.style.strokeDashoffset = String(314 - (314 * total) / 100);
  progressNote.textContent =
    total >= 80
      ? "You are in mock-exam territory. Keep the timer honest."
      : total >= 45
        ? "Good base. Add writing practice and timed reading next."
        : "Start with two lessons and one quiz round today.";
}

